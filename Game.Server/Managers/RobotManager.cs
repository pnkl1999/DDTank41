            List<int> roomType = new List<int> { 0, 4 };
            Random r = new Random();
            int countRoom = 0;
            for (int i = 0; i < countRoom; i++)
            {
                int randIndex = r.Next(roomType.Count);
                int random = roomType[randIndex];
                if (random == 0 || random == 4)
                {
                    listRoom.Add(new RobotRoom()
                    {
                        PlayerCount = 1,
                        MaxPlayerCount = 2,
                        RoomName = "DDTank NEWGUN",
                        RoomType = random
                    });
                }
            }

            if (listRoom == null)
            {
                listRoom = new List<RobotRoom>
                    {
                        new RobotRoom()
                    };
            }
            foreach (RobotRoom room in listRoom)
            {
                try
                {
                    int index = r.Next(RobotGamePlayers.Count);
                    RobotGamePlayer randomp = null;
                    int key1 = 0;
                    foreach (var abc in RobotGamePlayers)
                    {
                        randomp = abc.Value;
                        key1 = abc.Key;
                        break;
                    }
                    RobotGamePlayers.Remove(key1);
                    RoomMgr.CreateRoom(randomp, room.RoomName, "", (eRoomType)room.RoomType, (byte)1);

                    RoomMgr.WaitingRoom.RemovePlayer(randomp);
                }
                catch (Exception Err)
                {
                    log.Error("Error when load robot, robot name: " + room.RoomName, Err);
                }
            }
        }
